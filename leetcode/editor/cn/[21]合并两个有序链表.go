/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func mergeTwoLists(l1 *ListNode, l2 *ListNode) *ListNode {
	result := &ListNode{}
	var head  = result
	for l1 != nil && l2 != nil{
		if l1.Val > l2.Val {
			result.Next = &ListNode{Val: l2.Val}
			result = result.Next
			l2 = l2.Next
		}else{
			result.Next = &ListNode{Val: l1.Val}
			result = result.Next
			l1 = l1.Next
		}
	}
	if l1 != nil {
		result.Next = l1
	}else if l2 != nil {
		result.Next = l2
	}
	return  head.Next
}
