/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
var first *ListNode
func isPalindrome(head *ListNode) bool {
first = head
return check(head)
}
func check(head *ListNode)bool  {
if head == nil {
return true
}
subRes := check(head.Next)
if first.Val != head.Val {
return false
}
first = first.Next
return subRes
}
