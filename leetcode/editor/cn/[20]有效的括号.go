func isValid(s string) bool {
	leftStack := list.New()
	for _,ch := range  s {
		if ch=='(' ||ch=='[' || ch=='{' {
			leftStack.PushFront(ch)
		}else{
			if leftStack.Len() != 0 {
				top := leftStack.Front().Value.(rune)
				if top == leftOf(ch)  {
					leftStack.Remove(leftStack.Front())
				}else{
					return false
				}
			}else{
				return false
			}
		}
	}
	return leftStack.Len() == 0
}
func leftOf(ch rune) rune {
	switch ch {
	case ')':
		return '('
	case ']':
		return '['
	case '}':
		return '{'
	}
	return ' '
}
